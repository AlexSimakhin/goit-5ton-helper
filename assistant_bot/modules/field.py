"""Базовий клас для зберігання значення поля різних типів даних."""

class Field:
    """Базовий клас для зберігання значення поля різних типів даних."""

    def __init__(self, value):
        """Ініціалізує поле зі значенням."""
        self.value = value

    def __str__(self):
        """Повертає рядкове представлення значення поля."""
        return str(self.value)
